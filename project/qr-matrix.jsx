// Render helper — draws a dotted QR with rounded finder eyes.
// `size` = target px box. Returns an array of SVG elements (no surrounding <g>).
window.renderQRDots = function renderQRDots({ size, fill, centerOffset = true }) {
  const matrix = window.QR_MATRIX;
  const n = matrix.length; // 29
  const mod = size / n;
  const off = centerOffset ? -size / 2 : 0;
  const inFinder = (r, c) => (
    (r < 7 && c < 7) ||
    (r < 7 && c >= n - 7) ||
    (r >= n - 7 && c < 7)
  );
  const dots = [];
  for (let r = 0; r < n; r++) {
    for (let c = 0; c < n; c++) {
      if (matrix[r][c] && !inFinder(r, c)) {
        const cx = off + c * mod + mod / 2;
        const cy = off + r * mod + mod / 2;
        dots.push(
          React.createElement('circle', {
            key: `${r}-${c}`, cx, cy, r: mod * 0.42, fill,
          })
        );
      }
    }
  }
  const eyes = [[0, 0], [0, n - 7], [n - 7, 0]].map(([er, ec], i) => {
    const x = off + ec * mod;
    const y = off + er * mod;
    const s = 7 * mod;
    const innerOff = 2 * mod;
    const innerS = 3 * mod;
    return React.createElement('g', { key: `eye-${i}` },
      React.createElement('rect', {
        x, y, width: s, height: s,
        rx: mod * 1.8, ry: mod * 1.8,
        fill: 'none', stroke: fill, strokeWidth: mod,
      }),
      React.createElement('rect', {
        x: x + innerOff, y: y + innerOff,
        width: innerS, height: innerS,
        rx: mod * 0.9, ry: mod * 0.9, fill,
      })
    );
  });
  return [...dots, ...eyes];
};
